#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	j;

	j = 0;
	while (src[j])
		j++;
	if (dstsize == 0)
		return (j);
	j = 0;
	while (src[j] && j < dstsize - 1)
	{
		dst[j] = src[j];
		j++;
	}
	dst[j] = '\0';
	j = 0;
	while (src[j])
		j++;
	return (j);
}
