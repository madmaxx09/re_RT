#include <stdlib.h>

char	*ft_strrchr(const char *s, int c)
{
	int		j;
	char	i;

	j = 0;
	i = c;
	while (s[j])
		j++;
	while (j >= 0)
	{
		if (s[j] == i)
			return ((char *)s + j);
		j--;
	}
	return (NULL);
}
